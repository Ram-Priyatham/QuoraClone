<template>
  <div>
    {{ getAnswers }}
  </div>
</template>
<script>
import { mapActions, mapGetters } from "vuex";
export default {
  data() {
    return {};
  },
  computed: {
    ...mapGetters(["getAnswers"]),
  },
  methods: {
    ...mapActions(["getAnswers"]),
  },
  created() {
    this.$store.dispatch("getAnswersApi", {
      id: localStorage.getItem("email"),
    });
  },
};
</script>

